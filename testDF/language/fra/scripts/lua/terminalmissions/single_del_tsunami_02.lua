function DIALOG()

	NODE(0)
		SAY("Qu'est-ce que tu fais ici ?")
		SAY("Hé ho, je peux t'aider ?")
		SAY("Oui ? Qu'est-ce que tu veux, runner ?")
		SAY("Bienvenue. Qu'est-ce que je peux faire pour toi ?")
		SAY("Salut runner, tu viens pour la livraison, c'est ça ?")
		
		ANSWER("Oui, je suis là pour la livraison.",1)
		ANSWER("Ouais, c'est ça. Donnez-moi le colis et l'adresse...",1)
		ANSWER("Désolé, j'ai déjà un job.",4)
		ANSWER("Trouve-toi quelqu'un d'autre, ça m'intéresse pas.",4)
		ANSWER("Sérieusement, j'ai l'air de faire des livraisons ? Tu te trompes, tu sais.",4)
	NODE(1)
		GIVEQUESTITEM(91)
		SAY("Nous avons réussi à conclure une bonne affaire avec un certain %NPC_NAME(1), qui habite à %NPC_WORLD(1). Je veux que tu lui donnes ce colis et que tu files en vitesse, si j'étais toi j'attendrais pas qu'il l'ait ouvert. Reviens quand tu as fini.")
		SAY("Runner, J'ai un colis que je veux faire livrer à %NPC_NAME(1), %NPC_WORLD(1). C'est du matériel sensible, c'est le moins qu'on puisse dire, et je ne voudrais pas être dans le coin quand ça sera ouvert. Reviens me voir quand tu as fini.")
		SAY("Runner, le Syndicat a trouvé des infos passionnantes sur %NPC_NAME(1), à %NPC_WORLD(1). Tout se trouve dans ce colis, et je veux leur en faire cadeau. Tu leur donnes, et ensuite tu cours. Il vaut mieux pour toi être très loin quand ils l'ouvriront, parce qu'ils ne vont pas apprécier.")
		ACTIVATEDIALOGTRIGGER(0)
		SETNEXTDIALOGSTATE(2)
		ENDDIALOG()
	NODE(2)	
		ISMISSIONTARGETACCOMPLISHED(1)
			if (result==0) then
				SAY("T'as peur, ou quoi ? C'est pas dangereux si tu t'en vas en courant une fois que tu leur as donné le paquet. Allez, vas-y.")
				SAY("Dépêche-toi de me livrer ça. Je suis curieux de voir la réaction de %NPC_NAME(1).")
				SAY("T'as oublié où tu devais aller, mon guignol ? %NPC_NAME(1), %NPC_WORLD(1). Oublie pas de te faire la malle en vitesse, hein ?")
				SAY("Va livrer ça à %NPC_NAME(1). Aujourd'hui, c'est mieux.")
				ENDDIALOG()
			else
				SAY("Runner, %NPC_NAME(1) a confirmé la livraison, si on veut. Tu as bien travaillé, j'espère que ça s'est bien passé pour toi. J'ai fait verser %REWARD_MONEY() crédits sur ton compte. A la prochaine.")
				SAY("Hé, %NPC_NAME(1) m'a appelé, j'imagine que tu as fait la livraison. J'ai fait transférer %REWARD_MONEY() crédits sur ton compte. Tsunami te remercie pour le service rendu.")
				SAY("La livraison a été confirmée, j'ai fait verser %REWARD_MONEY() crédits sur ton compte. Tsunami te remercie pour ton aide. A bientôt.")
				SAY("Runner, %REWARD_MONEY() crédits ont été transférés sur ton compte. Tu as bien agi, on espère te revoir bientôt.")
				SAY("Salut Runner. Tu viens de gagner %REWARD_MONEY() crédits. Surveille les terminaux pour d'autres missions. A bientôt, j'espère.")
				ACTIVATEDIALOGTRIGGER(2)
				ENDDIALOG()
			end
	NODE(3)
		TAKEQUESTITEM(91)
			if (result==0) then
				SAY("Quoi ? J'attends pas de livraison. T'es certain que c'est pour moi ? Mouais, tu l'as même pas avec toi, ce paquet. Allez, dégage.")
				SAY("T'avais un paquet pour moi, non ? Ben il est où ?")
				SAY("Arrête de te moquer de moi, tu l'as pas, ce paquet.")
				ENDDIALOG()
			else	
				SAY("Quoi ? J'attendais pas de paquet... Je comprends pas... Ouais, celui qui a envoyé ça, il va payer !")
				SAY("Nan, tu t'es trompé de personne, j'attends rien du tout, moi.")
				SAY("C'est vrai ? Pour moi ? De la part de qui ? Hmmm mouais... Qui que ce soit, il va payer pour ça, je te le garantis.")
				SAY("Bon, ben merci, même si je m'attendais pas à ça. Celui qui t'a donné ça va le payer.")
				SAY("Oh, un cadeau. Je me demande ce que c'est...")
				ACTIVATEDIALOGTRIGGER(1)
				SETNEXTDIALOGSTATE(5)	
				ENDDIALOG()
			end
	NODE(4)
		SAY("Pour de l'information au sujet de Tsunami, je te suggère de regarder un terminal.")
		SAY("Si tu peux pas m'aider pour les livraisons, alors fiche le camp.")
		SAY("OK, j'ai compris. T'es un genre de touriste, c'est ça ? Dehors, avant que je te fasse sortir moi-même.")
		ENDDIALOG()
	NODE(5)
		SAY("Mais tu veux quoi encore ? Retourne voir %NPC_NAME(0) avant que je m'en charge moi-même.")
		SAY("Merci pour le cadeau, c'est gentil. %NPC_NAME(0) doit être fier de toi maintenant.") 
		ENDIALOG()
end
